public class Tiposvariaveis {
  public static void main(String[] args) {
   // Create variables of different data types
    int items = 15;
    float costPerItem = 30.20f;
    float totalCost = items * costPerItem;
    char currency = '$';

    // Print variables
    System.out.println("numero de roupas: " + items);
    System.out.println("preço por peça: " + costPerItem + currency);
    System.out.println("total de gasto= " + totalCost + currency);
  }
}
